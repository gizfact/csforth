//------------------------------------------------------------------------------
using System.Linq;
//------------------------------------------------------------------------------
namespace csforth
{
    //------------------------------------------------------------------------------
    static partial class Core
    {
        //------------------------------------------------------------------------------
        // STACK WORDS
        //------------------------------------------------------------------------------
        static int drop()
        {
            fStack.Pop();

            return 0;
        }
        //------------------------------------------------------------------------------
        static int dup()
        {
            fStack.Push(fStack.Peek());

            return 0;
        }
        //------------------------------------------------------------------------------
        static int dup2()
        {
            fStack.Push(fStack.ElementAt(1));
            fStack.Push(fStack.ElementAt(1));

            return 0;
        }
        //------------------------------------------------------------------------------
        static int swap()
        {
            object hi = fStack.Pop();
            object lo = fStack.Pop();

            fStack.Push(hi);
            fStack.Push(lo);

            return 0;
        }
        //------------------------------------------------------------------------------
        static int over()
        {
            fStack.Push(fStack.ElementAt(fStack.Count - 2));

            return 0;
        }
        //------------------------------------------------------------------------------
        static int rot()
        {
            object hi = fStack.Pop();
            object mi = fStack.Pop();
            object lo = fStack.Pop();

            fStack.Push(mi);
            fStack.Push(hi);
            fStack.Push(lo);

            return 0;
        }
        //------------------------------------------------------------------------------
        static int nrot()
        {
            object hi = fStack.Pop();
            object mi = fStack.Pop();
            object lo = fStack.Pop();

            fStack.Push(hi);
            fStack.Push(lo);
            fStack.Push(mi);

            return 0;
        }
        //------------------------------------------------------------------------------
    }
    //------------------------------------------------------------------------------
}
