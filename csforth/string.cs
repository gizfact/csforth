namespace csforth
{
    //------------------------------------------------------------------------------
    static partial class Core
    {
        //------------------------------------------------------------------------------
        // STRING WORDS
        //------------------------------------------------------------------------------
        static int schar()
        {
            int hi = (int)fStack.Pop();
            string lo = (string)fStack.Pop();

            fStack.Push((int)lo[hi]);

            return 0;
        }
        //------------------------------------------------------------------------------
        static int spos()
        {
            string hi = (string)fStack.Pop();
            string lo = (string)fStack.Pop();

            fStack.Push(lo.IndexOf(hi));

            return 0;
        }
        //------------------------------------------------------------------------------
    }
    //------------------------------------------------------------------------------
}
